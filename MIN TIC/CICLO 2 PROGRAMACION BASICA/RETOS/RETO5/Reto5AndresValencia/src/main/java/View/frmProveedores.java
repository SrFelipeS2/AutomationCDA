    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String nit = this.txtNit.getText();
        String razonZocial = this.txtRazonSocial.getText();
        String telefono = this.txtTelefono.getText();
        String correoelectronico = this.txtCorreo.getText();
        String representante = this.txtRepresentante.getText();
        String sitioWeb = this.txtSitioWeb.getText();
        String calle_Carrera = this.txtCllCra.getText();
        String numero = this.txtNumero.getText();
        String barrio = this.txtBarrio.getText();
        String ciudad = this.txtCiudad.getText();
        
        if(!nit.trim().matches("[0-9]{1,}")){
            JOptionPane.showMessageDialog(this, "Digite el nit de su empresa", "Administrador de proveedores", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(razonZocial.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Digite la Razon Social  de su empresa", "Administrador de proveedores", JOptionPane.ERROR_MESSAGE);
            return;
        }
         
        if(!telefono.trim().matches("[0-9]{1,}")){
            JOptionPane.showMessageDialog(this, "Digite el telefono de su empresa", "Administrador de proveedores", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(correoelectronico.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Digite el correo electronico de su empresa", "Administrador de proveedores", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(representante.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Digite el nombre del representante  de su empresa", "Administrador de proveedores", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(sitioWeb.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Digite la url de su pagina web  correspondiente a  su empresa", "Administrador de proveedores", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(calle_Carrera.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Digite la calle o la carrera de su empresa", "Administrador de Direcciones", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(numero.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Digite el numero asociado a la calle o carrera de la direccion de su empresa", "Administrador de Direcciones", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(barrio.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Digite el barrio en el que se ubica su empresa", "Administrador de Direcciones", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(ciudad.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Digite la ciudad donde se ubica su empresa", "Administrador de Direcciones", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
       Proveedores proveedor = new Proveedores();
       
       proveedor.setNit(nit);
       proveedor.setRazonSocial(razonZocial);
       proveedor.setTelefono(telefono);
       proveedor.setCorreoElectronico(correoelectronico);
       proveedor.setRepresentante(representante);
       proveedor.setSitioWeb(sitioWeb);
       proveedor.setCalle_Carrera(calle_Carrera);
       proveedor.setNumero(numero);
       proveedor.setBarrio(barrio);
       proveedor.setCiudad(ciudad);
        
       //comunicacion con el controlador para enviar la base de datos 
        boolean result = ctlProveedores.RegistrarProveedor(proveedor);
        if(result){
            JOptionPane.showMessageDialog(this, "proveedor registrado con exito");
        }else{
            JOptionPane.showMessageDialog(this, "proveedor no pudo ser creado", "Administrador de Proovedores", JOptionPane.ERROR_MESSAGE);

        }
        
                
        this.cleanForm();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String Nit = this.txtNit.getText();
        
        if(!Nit.trim().matches("[0-9]{1,}")){
            JOptionPane.showMessageDialog(this, "digite el nit en numeros", "Administrador de proveedores",JOptionPane.ERROR_MESSAGE);
            return;
        }   
        this.cleanForm();
        Proveedores OProveedor = ctlProveedores.BuscarProveedor(Nit);
        if(OProveedor != null){
            
          this.txtNit.setText(OProveedor.getNit());
          this.txtRazonSocial.setText(OProveedor.getRazonSocial());
          this.txtTelefono.setText(OProveedor.getTelefono());
          this.txtCorreo.setText(OProveedor.getCorreoElectronico());
          this.txtRepresentante.setText(OProveedor.getRepresentante());
          this.txtSitioWeb.setText(OProveedor.getSitioWeb());
          this.txtCllCra.setText(OProveedor.getCalle_Carrera());
          this.txtNumero.setText(OProveedor.getNumero());
          this.txtBarrio.setText(OProveedor.getBarrio());
          this.txtCiudad.setText(OProveedor.getCiudad());
   
        }
        else{
            JOptionPane.showMessageDialog(this, "No esxiste el proveedor con el nit: "  + Nit, " Administrador de proveedores",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

