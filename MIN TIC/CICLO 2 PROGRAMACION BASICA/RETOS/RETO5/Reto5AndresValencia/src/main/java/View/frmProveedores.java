    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
         String nit = this.txtNit.getText();
        
        if(!nit.trim().matches("[0-9]{1,}")){
            JOptionPane.showMessageDialog(this, "digite el nit en numeros", "gestion de proveedores",JOptionPane.ERROR_MESSAGE);
            return;
        } 
        
        Proveedores result = ctlProveedores.BuscarProveedor(nit);
        if(result != null){
            boolean resulDelete = ctlProveedores.EliminarProveedor(nit);
            if(resulDelete)
                JOptionPane.showMessageDialog(this, "Se elimino con exito ", " Administrador de proveedores",JOptionPane.INFORMATION_MESSAGE);
            else
                JOptionPane.showMessageDialog(this, "ocurrio un error al tratar de eliminar el proveedor ", " Administrador de proveedores", JOptionPane.ERROR_MESSAGE);

        }else{
        
            JOptionPane.showMessageDialog(this, "No se encontro ningun Proveedor con ese NIT ", " Administrador de proveedores", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnEliminarActionPerformed
    
    
     
